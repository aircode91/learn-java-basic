public class TypeDataCharacter {
    public static void main(String[] args) {
        char a = 'A';
        char g = 'G';
        char u = 'U';
        char s = 'S';

        System.out.print(a);
        System.out.println(g);
        System.out.println(u);
        System.out.println(s);
        System.out.println(a + g + u + s); // Output 304
        System.out.print(a);
        System.out.print(g);
        System.out.print(u);
        System.out.print(s);
    }
}
